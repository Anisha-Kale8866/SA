package loops;

public class DoWhileLoopStudy {

	public static void main(String[] args) {
		// // 
		
		// I want to print 1 to 10 numbers
		int i=1;
		
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=10);
		
		//30-->1
		int j=30;
		
		do
		{
			System.out.println(j);
			j=j-3;
		}
		while(j>=1);
		
		
		// i want to Print 2 table
		
	}

}
